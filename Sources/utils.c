#include "../Includes/main.h"

/* ----------------------------------------------------------------------------
Free a matrix.
---------------------------------------------------------------------------- */

void	ft_free_mat(char **mat)
{
	int	i;

	if (!mat)
		return ;
	i = -1;
	while (mat[++i])
		free(mat[i]);
	free(mat);
	return ;
}
